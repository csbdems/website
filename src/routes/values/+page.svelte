			<div id="values">
				<h1>Our Values</h1>

				<div id="values-statement">
					<p>
						Cheltenham schools thrive because of ongoing community <em>collaboration</em>. School
						Directors help establish a shared vision of the <em>transformation</em> happening within
						our schools. A shared vision keeps the focus on <em>inclusion</em>, ensuring that all
						voices are heard and all students are equipped to succeed.
					</p>
				</div>

				<div>
					<h2>
						1) Enabling a vibrant and connected Cheltenham through
						<em>high quality public education</em>.
					</h2>
					<p>
						Every child deserves an education that helps prepare them to be self-determined,
						resilient, and engaged citizens. We commit to rooting our decisions in one’s lived
						experience to ensure that schools are well-run and support every student in achieving
						their potential.
					</p>
				</div>

				<div>
					<h2>2) Achieving district priorities via <em>strong fiscal management</em>.</h2>
					<p>
						Education represents one of the most important investments we make as a community. We
						commit to establishing budgets that address the most critical factors that contribute to
						success while recognizing the existing tax burden of Cheltenham residents.
					</p>
				</div>

				<div>
					<h2>3) Investing in schools where <em>all students learn and grow</em>.</h2>
					<p>
						All students can succeed. We commit to building on the work of creating a culture that
						honors the dignity of every human being and serves the holistic needs of students and
						staff.
					</p>
				</div>

				<div>
					<h2>
						4) Creating an <em>environmentally aware and responsible</em> school district.
					</h2>
					<p>
						Students spend most of their days in school during some of the most important
						developmental years. We commit to developing environmentally sensitive schools that are
						safe, inspiring, and a foundation for social awareness and responsibility.
					</p>
				</div>

				<div>
					<h2>5) <em>Engaging the community</em>, all year round.</h2>
					<p>
						Strong schools are built in partnership with family and community members. We commit to
						sustaining meaningful partnerships, listening, and learning from those most impacted by
						the Board’s decisions.
					</p>
				</div>
			</div>
